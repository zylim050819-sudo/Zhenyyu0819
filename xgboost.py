# Save the Pipeline (Scaler + XGBoost)
joblib.dump(best_model, "xgboost_model.joblib")

# Save the exact feature columns and labels
metadata = {
    "feature_columns": list(X.columns),
    "class_labels": class_labels
}

with open("xgboost_metadata.json", "w") as f:
    json.dump(metadata, f)

print("Export complete! 'xgboost_model.joblib' and 'xgboost_metadata.json' are ready.")